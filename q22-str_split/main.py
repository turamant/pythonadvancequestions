"""Функция split() в Python используется для разделения строки на подстроки на основе
указанного разделителя. Результатом является список подстрок.

Основные моменты:

Функция split() разделяет строку на список подстрок на основе указанного разделителя
(по умолчанию - пробельные символы).
Вы можете указать пользовательский разделитель, например, запятую или дефис.
Можно ограничить количество разделений с помощью дополнительного аргумента.
Функция splitlines() разделяет многострочную строку по символам новой строки.
Таким образом, split() - это мощный инструмент для работы со строками, позволяющий
разбивать их на удобные для обработки части.
Вот как использовать функцию split():"""


# Базовое использование split()
text = " Hello, World ! "
words = text.split() # Разделение по пробелам
print(words) # ['Hello,', 'World!']

# Использование пользовательского разделителя
date = "2023-10-08"
parts = date.split('-') # Разделение по дефису
print(parts) # ['2023', '10', '08']

# Ограничение количества разделений
text = "apple, banana, cherry, date, fig"
fruits = text.split(',', 2) # Разделение по запятой, максимум 2 разделения
print(fruits) # ['apple', 'banana', 'cherry, date, fig']

# Разделение строк в многострочной строке
multiline_text = "Line 1\nLine 2\nLine 3"
lines = multiline_text.splitlines()
print(lines) # ['Line 1', 'Line 2', 'Line 3']
